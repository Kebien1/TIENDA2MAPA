CREATE OR ALTER procedure SpSiguienteCodigoCliente
as
begin
	select max (CODCLI)+1 from CLIENTES2
end

EXEC SpSiguienteCodigoCliente

CREATE OR ALTER procedure SpInsertarCliente
@Nom varchar(30),
@Gen Varchar(10),
@Lat Float,
@Lon Float
as
begin
	insert into CLIENTES2 values (@Nom, @Gen, @Lat, @Lon)
end

