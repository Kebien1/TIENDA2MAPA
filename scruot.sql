CREATE OR ALTER procedure SpModificaCliente
@Cod int,
@Nom varchar(30),
@Gen Varchar(10),
@Lat Float,
@Lon Float
as
begin
	update CLIENTES2 set Nombre = @Nom, Genero = @Gen, Latitud =  @Lat, LONGITUD =  @Lon 
	Where CODCLI = @Cod
end

CREATE OR ALTER procedure SpEliminaCliente
@Cod int
as
begin
	delete from CLIENTES2 Where CODCLI = @Cod
end
