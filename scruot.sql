CREATE OR ALTER PROCEDURE SPBUSCARCLIENTES
@Nom VARCHAR(30)
AS
BEGIN
 SELECT * FROM CLIENTES2 WHERE NOMBRE LIKE '%'+@Nom+'%'
END

